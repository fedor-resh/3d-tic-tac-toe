export function countScore(d){
    const lines = [
        // вершины
        [0, 1, 2], [0, 4, 8], [0, 3, 6], [0, 12, 24], [0, 9, 18], [0, 10, 20], [0, 13, 26],
        [18, 19, 20], [18, 10, 2], [18, 12, 6], [18, 21, 24], [18, 22, 26], [18, 13, 8],
        [20, 11, 2], [20, 22, 24], [20, 23, 26], [20, 14, 8], [20, 13, 6],
        [2, 14, 26], [2, 5, 8], [2, 4, 6], [2, 13, 24],
        [6, 15, 24], [6, 16, 26], [6, 7, 8],
        [24, 25, 26], [24, 16, 8],
        [26, 17, 8],
        // середины ребер
        [1, 10, 19], [1, 13, 25], [1, 4, 7],
        [19, 13, 7], [19, 22, 25],
        [25, 16, 7],

        [11, 10, 9], [11, 13, 15], [11, 14, 17],
        [9, 13, 17], [9, 12, 15],
        [15, 16, 17],

        [3, 4, 5], [3, 13, 23], [3, 12, 21],
        [21, 13, 5], [21, 22, 23],
        [23, 14, 5],
        // середины граней
        [4, 13, 22], [10, 13, 16], [12, 13, 14]
    ];
    let count_won_1 = 0;
    let count_won_2 = 0;
    for (let l of lines) {
        if (d[l[0]] === d[l[1]] && d[l[1]] === d[l[2]]){
            if (d[l[0]] === 1)
                count_won_1 ++
            if (d[l[0]] === -1)
                count_won_2 ++
        }
    }
    return {first: count_won_1, second: count_won_2}
}